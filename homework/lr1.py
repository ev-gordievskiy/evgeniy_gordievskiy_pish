def rasklad():
    try:
        a = int(input("Введите целое число: ")) # Ввод числа
        b = [int(c) for c in str(abs(a))]   # Представление числа в виде строки с набором цифр        
        proverka = True
        for i in range(len(b) - 1): # Присвоение индексов каждой цифре в наборе
            if b[i] > b[i + 1]: # Сравнение каждой последующей цифры с предыдущей
                proverka = False
                break
        
        if proverka:
            print("Цифры числа", a, "расположены в порядке возрастания") 
        else:
            print("Цифры числа", a, "расположены не в порядке возрастания")
            
    except ValueError:
        print("Неверный тип данных")

rasklad()