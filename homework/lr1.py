try:
    a = int(input("Введите натуральное число от 1 до 100: "))    # Ввод числа а
    if a > 100: # Проверка, что число а не больше 100
        print("Число а не должно быть больше 100!")
    else:
        print("Для каждого числа от 1 до", a, "квадрат и куб числа: ")
        for i in range (1, a + 1):  # Цикл от 1 до а
            print(i, "->", i ** 2, i ** 3)  # Вывод кубов и квадратов

except ValueError:
    print("Неверный тип данных")
    

